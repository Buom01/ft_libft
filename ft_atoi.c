#include "stddef.h"

int	ft_atoi(const char *str)
{
	size_t	nb;
	char	sign;
	char	*c;

	nb = 0;
	sign = 1;
	c = (char*)str;
	while (*c == '-' || *c == '+' || *c == ' ' || (*c >= '\t' && *c <= '\r'))
		if (*(c++) == '-')
			sign *= -1;
	while (*c >= '0' && *c <= '9')
		nb = (nb * 10 + (*(c++) - '0') * sign);
	return ((int)nb);
}
