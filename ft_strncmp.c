#include "stddef.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	char	*s3;
	char	*s4;

	s3 = (char*)s1;
	s4 = (char*)s2;
	while (*s3 == *s4 && (n--) > 0)
	{
		s3++;
		s4++;
	}
	return ((int)(*s3) - (int)(*s4));
}
