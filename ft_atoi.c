int	ft_atoi(const char *str)
{
	long	nb;
	char	sign;
	char	*c;

	nb = 0;
	sign = 1;
	c = (char*)str;
	while (*c == '-' || *c == '+' || *c == ' ' || (*c >= '\t' && *c <= '\r'))
	{
		if (*c == '-')
			sign *= -1;
		c++;
	}
	while (*c >= '0' || *c <= '9')
	{
		nb += (*c - '0') * sign;
		nb *= 10;
		c++;
	}
	return ((int)nb);
}
