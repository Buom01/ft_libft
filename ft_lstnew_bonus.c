#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	if (!(lst = malloc(sizeof(ft_lstnew))))
		return (NULL);
	lst->content = content;
	lst->next = NULL;
	return (lst);
}
